package classifier;

public class PhotoForBrowser {
private String id;
private String path;
private String name;
private String date;


public String getId() {
	return id;
}

public void setId(String id) {
	this.id = id;
}

public String getPath() {
	return path;
}

public void setPath(String path) {
	this.path = path;
}

public String getDate() {
	return date;
}

public void setDate(String date) {
	this.date = date;
}

public String getName() {
	return name;
}

public void setName(String name) {
	this.name = name;
}

} // End of class
